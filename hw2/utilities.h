#define PI 3.141592654

//the length of each link in the arm (should be the same as the one used in runtest.m)
#define LINKLENGTH_CELLS 10

typedef struct {
  int X1, Y1;
  int X2, Y2;
  int Increment;
  int UsingYIndex;
  int DeltaX, DeltaY;
  int DTerm;
  int IncrE, IncrNE;
  int XIndex, YIndex;
  int Flipped;
} bresenham_param_t;


void ContXY2Cell(double x, double y, short unsigned int* pX, short unsigned int *pY, int x_size, int y_size)
{
    double cellsize = 1.0;
	//take the nearest cell
	*pX = (int)(x/(double)(cellsize));
	if( x < 0) *pX = 0;
	if( *pX >= x_size) *pX = x_size-1;

	*pY = (int)(y/(double)(cellsize));
	if( y < 0) *pY = 0;
	if( *pY >= y_size) *pY = y_size-1;
}


void get_bresenham_parameters(int p1x, int p1y, int p2x, int p2y, bresenham_param_t *params)
{
  params->UsingYIndex = 0;

  if (fabs((double)(p2y-p1y)/(double)(p2x-p1x)) > 1)
    (params->UsingYIndex)++;

  if (params->UsingYIndex)
    {
      params->Y1=p1x;
      params->X1=p1y;
      params->Y2=p2x;
      params->X2=p2y;
    }
  else
    {
      params->X1=p1x;
      params->Y1=p1y;
      params->X2=p2x;
      params->Y2=p2y;
    }

   if ((p2x - p1x) * (p2y - p1y) < 0)
    {
      params->Flipped = 1;
      params->Y1 = -params->Y1;
      params->Y2 = -params->Y2;
    }
  else
    params->Flipped = 0;

  if (params->X2 > params->X1)
    params->Increment = 1;
  else
    params->Increment = -1;

  params->DeltaX=params->X2-params->X1;
  params->DeltaY=params->Y2-params->Y1;

  params->IncrE=2*params->DeltaY*params->Increment;
  params->IncrNE=2*(params->DeltaY-params->DeltaX)*params->Increment;
  params->DTerm=(2*params->DeltaY-params->DeltaX)*params->Increment;

  params->XIndex = params->X1;
  params->YIndex = params->Y1;
}

void get_current_point(bresenham_param_t *params, int *x, int *y)
{
  if (params->UsingYIndex)
    {
      *y = params->XIndex;
      *x = params->YIndex;
      if (params->Flipped)
        *x = -*x;
    }
  else
    {
      *x = params->XIndex;
      *y = params->YIndex;
      if (params->Flipped)
        *y = -*y;
    }
}

int get_next_point(bresenham_param_t *params)
{
  if (params->XIndex == params->X2)
    {
      return 0;
    }
  params->XIndex += params->Increment;
  if (params->DTerm < 0 || (params->Increment < 0 && params->DTerm <= 0))
    params->DTerm += params->IncrE;
  else
    {
      params->DTerm += params->IncrNE;
      params->YIndex += params->Increment;
    }
  return 1;
}



int IsValidLineSegment(double x0, double y0, double x1, double y1, double*	map,
		   int x_size,
 		   int y_size)

{
	bresenham_param_t params;
	int nX, nY;
    short unsigned int nX0, nY0, nX1, nY1;

    //printf("checking link <%f %f> to <%f %f>\n", x0,y0,x1,y1);

	//make sure the line segment is inside the environment
	if(x0 < 0 || x0 >= x_size ||
		x1 < 0 || x1 >= x_size ||
		y0 < 0 || y0 >= y_size ||
		y1 < 0 || y1 >= y_size)
		return 0;

	ContXY2Cell(x0, y0, &nX0, &nY0, x_size, y_size);
	ContXY2Cell(x1, y1, &nX1, &nY1, x_size, y_size);

    //printf("checking link <%d %d> to <%d %d>\n", nX0,nY0,nX1,nY1);

	//iterate through the points on the segment
	get_bresenham_parameters(nX0, nY0, nX1, nY1, &params);
	do {
		get_current_point(&params, &nX, &nY);
		if(map[GETMAPINDEX(nX,nY,x_size,y_size)] == 1)
            return 0;
	} while (get_next_point(&params));

	return 1;
}

int IsValidArmConfiguration(double* angles, int numofDOFs, double*	map,
		   int x_size, int y_size)
{
    double x0,y0,x1,y1;
    int i;

 	//iterate through all the links starting with the base
	x1 = ((double)x_size)/2.0;
    y1 = 0;
	for(i = 0; i < numofDOFs; i++)
	{
		//compute the corresponding line segment
		x0 = x1;
		y0 = y1;
		x1 = x0 + LINKLENGTH_CELLS*cos(2*PI-angles[i]);
		y1 = y0 - LINKLENGTH_CELLS*sin(2*PI-angles[i]);

		//check the validity of the corresponding line segment
		if(!IsValidLineSegment(x0,y0,x1,y1,map,x_size,y_size))
				return 0;
	}

  return 1;
}

struct Vertex{
  std::vector<double> angles_;
  std::vector<Vertex*> connected_;
  std::vector<double> costs_to_connected_;
  double g_value_ = HUGE_VAL;
  double h_value_ = HUGE_VAL;
  Vertex* parent_ = NULL;
  bool closed_ = false;
  bool open_listed_ = false;

  Vertex(int numofDOFs) {
    angles_.resize(numofDOFs);
    connected_.clear();
  }

  Vertex(double* angles, int numofDOFs) {
    angles_.resize(numofDOFs);
    angles_.assign(angles, angles+numofDOFs);
    connected_.clear();
  }

  double* getAnglesPtr() {
    return &angles_[0];
  }

  void resetStatus() {
    g_value_ = HUGE_VAL;
    h_value_ = HUGE_VAL;
    parent_ = NULL;
    closed_ = false;
    open_listed_ = false;
  }
};

bool compareVertexPtr(Vertex* a, Vertex* b) {
  return a->g_value_ + a->h_value_ > b->g_value_ + b->h_value_;
}

double dRand(double min, double max)
{
    double r = (double)rand() / RAND_MAX;
    return min + r * (max - min);
}

void getRandomVertex(int numofDOFs, Vertex* v) {
  for(int i=0; i<numofDOFs; i++) {
    v->angles_[i] = dRand(0.0, 2*PI);
  }
}

double getDistance(Vertex* v1, Vertex* v2) {
  double sum = 0.0;
  int DOF = v1->angles_.size();
  for(int i=0; i<DOF; i++) {
    double diff = v2->angles_[i] - v1->angles_[i];
    sum += diff * diff;
  }

  return sqrt(sum);
}

bool connect(Vertex* v1, Vertex* v2, int numofDOFs, double *map, int x_size, int y_size) {
  std::vector<double> left = v1->angles_;
  std::vector<double> right = v2->angles_;
  bool status = true;
  for(int i=0; i<4; i++) {
    std::vector<double> interpolated_angles(numofDOFs);
    for(int d=0; d<numofDOFs; d++) {
      interpolated_angles[d] = (left[d] + right[d]) / 2;
    }
    if(!IsValidArmConfiguration(&interpolated_angles[0], numofDOFs, map, x_size, y_size)) {
      status = false;
      break;
    } else {
      left = interpolated_angles;
    }
  }
  return status;
}


